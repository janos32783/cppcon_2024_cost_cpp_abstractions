// GPIO register base addresses
constexpr std::uint32_t GPIOF_BASE_ADDR = UINT32_C(0x48001400);
constexpr std::uint32_t GPIOD_BASE_ADDR = UINT32_C(0x48000c00);
constexpr std::uint32_t GPIOC_BASE_ADDR = UINT32_C(0x48000800);
constexpr std::uint32_t GPIOB_BASE_ADDR = UINT32_C(0x48000400);
constexpr std::uint32_t GPIOA_BASE_ADDR = UINT32_C(0x48000000);

template <std::uint32_t GPIOx_BASE_ADDR>
concept is_valid_gpio_base_address = (
    (GPIOx_BASE_ADDR == GPIOF_BASE_ADDR) ||
    (GPIOx_BASE_ADDR == GPIOD_BASE_ADDR) ||
    (GPIOx_BASE_ADDR == GPIOC_BASE_ADDR) ||
    (GPIOx_BASE_ADDR == GPIOB_BASE_ADDR) ||
    (GPIOx_BASE_ADDR == GPIOA_BASE_ADDR)
);

enum class gpio_ports : std::uint8_t {
    gpiof,
    gpiod,
    gpioc,
    gpiob,
    gpioa
};

template <gpio_ports port>
concept is_valid_gpio_port = (
    (port == gpio_ports::gpiof) ||
    (port == gpio_ports::gpiod) ||
    (port == gpio_ports::gpioc) ||
    (port == gpio_ports::gpiob) ||
    (port == gpio_ports::gpioa)
);

